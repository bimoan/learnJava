package basic_java;

public class for_loop_perulangan {

	public static void main(String[] args) {
		
		//for(inisialisasi; kondisi; iterasi){   }
		
		for(int i = 0; i <= 5; i++ ){
			System.out.println(i);
		}
		
		/* Perulangan For Loop, Terlebih Dahulu Menguji Syaratnya,
		 * apabila syaratnya terpenuhi baru kemudian akan memproses aksinya.
		 */

	}

}
