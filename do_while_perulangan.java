package basic_java;

public class do_while_perulangan {

	public static void main(String[] args) {
	
		int i = 0;
		
		do{
			System.out.println(i);
			i++;
		}while(i<5);
		
		/* Perbedaan perulangan Do While Dengan perulangan lainnya adalah 
		 * Perulangan Do while akan terlebih dahulu memperoses aksinya baru kemudian syaratnya.
		 */
		

	}
}
